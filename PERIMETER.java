public class PERIMETER {
    public static void main(String[] args) {
        // CREATE VARIABLE A and B
        int A = 2;
        int B = 5;

        //SUM UP TWO NUMBERS(VARIABLES)
        int SUM = A + B ;

        //PRINT THE SUM
        System.out.println("SUM IS " + SUM);

        // FIND THE PERIMETER AND AREA OF A SQUARE
        int Length = 15;
        int Breadth = 15;

        // CALCULATING THE PERIMETER AND AREA OF A SQUARE
        int PERIMETER = Length + Breadth + Length + Breadth ;
        int AREA = Length * Breadth ;

        // PRINT THE PERIMETER AND AREA OF A SQUARE
        System.out.println("PERIMETER IS " + PERIMETER);
        System.out.println("AREA IS " + AREA);
    }

}
